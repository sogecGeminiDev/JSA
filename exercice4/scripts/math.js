define(function(){
    return {
        add: function(a, b) {
            return parseInt(a) + parseInt(b);
        },
        subtract: function(a, b){
            return parseInt(a) + parseInt(b);
        },
        multiply:function(a, b){
            return parseInt(a) * parseInt(b);
        },
        divide: function(a, b){
            return parseInt(a) / parseInt(b);
        }
    }
});